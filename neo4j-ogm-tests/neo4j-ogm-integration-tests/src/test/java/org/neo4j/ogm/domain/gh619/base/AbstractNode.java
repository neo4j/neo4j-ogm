package org.neo4j.ogm.domain.gh619.base;

import java.util.Set;

import org.neo4j.ogm.annotation.Labels;
import org.neo4j.ogm.annotation.Property;

/**
 * NOTE: Those are classes from a bug report and we do _not_ recommend or endorse such a structure and naming!
 */
public abstract class AbstractNode extends AbstractGraph {
    @Labels
    protected Set<String> labels;
    @Property(name = "status")
    private String status;

    public Set<String> getLabels() {
        return labels;
    }

    public void setLabels(Set<String> labels) {
        this.labels = labels;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
